# Словарь для расшифровки ОПФ (аббревиатура → полная форма)
OPF_EXPANSION = {
    'ООО': 'ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ',
    'ЗАО': 'ЗАКРЫТОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО',
    'ОАО': 'ОТКРЫТОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО',
    'ПАО': 'ПУБЛИЧНОЕ АКЦИОНЕРНОЕ ОБЩЕСТВО',
    'АО': 'АКЦИОНЕРНОЕ ОБЩЕСТВО',
    'ИП': 'ИНДИВИДУАЛЬНЫЙ ПРЕДПРИНИМАТЕЛЬ',
    'НКО': 'НЕКОММЕРЧЕСКАЯ ОРГАНИЗАЦИЯ',
    'ГУП': 'ГОСУДАРСТВЕННОЕ УНИТАРНОЕ ПРЕДПРИЯТИЕ',
    'МУП': 'МУНИЦИПАЛЬНОЕ УНИТАРНОЕ ПРЕДПРИЯТИЕ',
    'ТСЖ': 'ТОВАРИЩЕСТВО СОБСТВЕННИКОВ ЖИЛЬЯ',
    'СНТ': 'САДОВОДЧЕСКОЕ НЕКОММЕРЧЕСКОЕ ТОВАРИЩЕСТВО',
    'КФХ': 'КРЕСТЬЯНСКОЕ ФЕРМЕРСКОЕ ХОЗЯЙСТВО',
    'ПК': 'ПРОИЗВОДСТВЕННЫЙ КООПЕРАТИВ',
    'ТОО': 'ТОВАРИЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ',
}


def clean_full_name(name):
    """
    Нормализует полное название компании:
    - Расшифровывает аббревиатуры ОПФ
      (ООО → ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ)
    - Если ОПФ уже расшифрована — оставляет как есть
    - Убирает кавычки
    - Убирает двойные пробелы
    - Приводит к верхнему регистру
    
    Примеры:
        'ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ "СТАНКОИМПОРТ"'
            → 'ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ СТАНКОИМПОРТ'
        'ООО "СТАНКОИМПОРТ"'
            → 'ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ СТАНКОИМПОРТ'
        'ООО ПО СМЗ'
            → 'ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ ПРОИЗВОДСТВЕННОЕ ОБЪЕДИНЕНИЕ СМЗ'
    """
    if pd.isna(name):
        return ''
    
    name = name.strip()
    
    # 1. Убираем кавычки
    name = name.strip('"\'«»„“')
    name = name.replace('"', '').replace("'", '').replace('«', '').replace('»', '')
    
    # 2. Ищем аббревиатуру ОПФ в начале строки и заменяем на полную форму
    words = name.split()
    if words:
        first_word = words[0].upper().strip('"\'«»„“.,;')
        if first_word in OPF_EXPANSION:
            # Заменяем только если это реально аббревиатура, а не часть расшифровки
            # Проверяем, что это не начало уже расшифрованной формы
            full_form = OPF_EXPANSION[first_word]
            # Убираем аббревиатуру и вставляем полную форму
            rest = ' '.join(words[1:])
            name = f"{full_form} {rest}"
    
    # 3. Убираем запятые и прочий мусор
    name = re.sub(r'[,;]+', ' ', name)
    
    # 4. Убираем лишние пробелы
    name = re.sub(r'\s+', ' ', name)
    
    # 5. Приводим к верхнему регистру
    name = name.upper()
    
    return name.strip()


