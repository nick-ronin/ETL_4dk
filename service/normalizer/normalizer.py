import pandas as pd
import re

def clean_phone(phone_raw):
    """
    Приводит номера телефонов к формату +7XXXXXXXXXX.
    Поддерживает разделители: ; , / |.
    Номера, начинающиеся с 8, заменяются на +7.
    Если номер уже содержит +, он сохраняется.
    Несколько номеров в одной ячейке объединяются через ', '.
    """
    if pd.isna(phone_raw):
        return phone_raw

    # Разбиваем по основным разделителям (точка с запятой, запятая, слэш, вертикальная черта)
    parts = re.split(r'[;,/|]\s*', phone_raw.strip())
    cleaned_parts = []

    for part in parts:
        if not part.strip():
            continue

        # Проверяем, есть ли '+' в начале
        has_plus = part.strip().startswith('+')

        # Удаляем все нецифровые символы
        digits = re.sub(r'\D', '', part)

        if not digits:
            continue

        # Если номер начинается с 8, заменяем на 7 (для российских номеров)
        if digits.startswith('8'):
            digits = '7' + digits[1:]

        # Добавляем '+' в зависимости от исходного формата
        if has_plus:
            # Если уже был плюс, но номер начинался с 8 после очистки, мы уже исправили
            cleaned = '+' + digits
        else:
            # Если номер начинается с 7, добавляем '+'
            if digits.startswith('7'):
                cleaned = '+' + digits
            else:
                cleaned = digits

        cleaned_parts.append(cleaned)

    # Объединяем части через запятую с пробелом
    return ', '.join(cleaned_parts) if cleaned_parts else None


def clean_inn(inn_raw):
    if pd.isna(inn_raw):
        return None
    
    # что будет возвращать
    inn_result = ""
    
    inn_str = str(inn_raw).strip()
    
    # Удаляем все нецифровые символы
    inn_result = re.sub(r'\D', '', inn_str)
    
    if len(inn_result) != 10 and len(inn_result) != 12:
        return "Неверный формат ИНН"
    
    if inn_result[:2] == "00":
        return "Неверный формат ИНН"
    
    return inn_result



def clean_email(email_str):
    """
    Метод очистки адресов электронной почты.

    Принимает:
        email_str (str) - строку с одним или несколькими email.
        Email могут быть разделены символами ';', ',', '/', '|'
        или переносом строки.

    Выполняет:
        - проверяет корректность входных данных;
        - приводит email к нижнему регистру;
        - удаляет лишние пробелы;
        - извлекает корректные email;
        - удаляет повторяющиеся адреса;
        - объединяет найденные email через запятую.

    Возвращает:
        str  - строку с очищенными email;
        None - если корректные email не найдены;
        исходное значение - если оно не является строкой или равно NaN.
    """

    # Если в ячейке пусто (NaN) или там не текст (например, число)
    # то ничего не делаем и возвращаем как есть, чтобы не было ошибки
    if pd.isna(email_str) or not isinstance(email_str, str):
        return email_str

    # Шаблон, который описывает, как должен выглядеть правильный email
    pattern = r'(?i)[A-Za-zа-яё0-9._%+-]+@[A-Za-zа-яё0-9.-]+\.[A-Za-zа-яё]{2,}'

    # Режем длинную строку на кусочки, если там используются разделители: ; , / | или перенос строки
    parts = re.split(r'[;,/|\n]+', email_str)

    cleaned_parts = []  # Сюда будем складывать только хорошие email
    seen = set()        # Список "уже увиденных", чтобы не повторяться

    for part in parts:
        # Убираем лишние пробелы по бокам и делаем все буквы маленькими
        part = part.strip().lower()

        # Если после очистки кусок оказался пустым — пропускаем его
        if not part:
            continue

        # Ищем внутри этого кусочка всё, что похоже на email по нашему шаблону
        emails = re.findall(pattern, part)

        for email in emails:
            # Если мы этот email еще не встречали раньше...
            if email not in seen:
                seen.add(email)          # Запоминаем его, чтобы не брать повторно
                cleaned_parts.append(email)  # Добавляем в наш список чистых email

    # Если нашли хоть один email — склеиваем их через запятую
    # Если ничего не нашли — возвращаем "Ничего" (None)
    return ', '.join(cleaned_parts) if cleaned_parts else None

# Список ОПФ для удаления из короткого названия
OPF_LIST = [
    "ООО", "ЗАО", "ОАО", "ПАО", "АО", "ИП", "НКО", "ГУП", "МУП",
    "ТСЖ", "СНТ", "КФХ", "ПК", "ПТ", "ТОО", "ТСН"
]

def clean_short_name(name):
    """
    Очищает короткое название компании:
    - Убирает ОПФ в любом месте (ООО, АО и т.д.)
    - Убирает кавычки
    - Убирает запятые и текст после них (как в "ЕДС-Щелково, ООО, единая диспетчерская служба")
    - Удаляет двойные пробелы, trim
    - Приводит к верхнему регистру для единообразия (опционально)
    
    Примеры:
        'ООО "СТАНКОИМПОРТ"'         → 'СТАНКОИМПОРТ'
        'ЕДС-Щелково, ООО, ...'     → 'ЕДС-ЩЕЛКОВО'
        'Picaso 3d'                 → 'PICASO 3D'
        'А-сбыт, торговая компания' → 'А-СБЫТ'
    """
    if pd.isna(name):
        return ''
    
    name = name.strip()
    
    # 1. Обрезаем по запятой (всё, что после запятой — мусор)
    if ',' in name:
        name = name.split(',')[0]
    
    # 2. Убираем кавычки
    name = name.strip('"\'«»„“')
    name = name.replace('"', '').replace("'", '').replace('«', '').replace('»', '')
    
    # 3. Убираем ОПФ (как отдельные слова)
    words = name.split()
    words_clean = []
    for w in words:
        w_clean = w.strip('"\'«»„“.,;')
        if w_clean.upper() not in OPF_LIST:
            words_clean.append(w)
    
    if not words_clean:
        return ''
    
    name = ' '.join(words_clean)
    
    # 4. Убираем оставшиеся кавычки и лишние пробелы
    name = name.strip('"\'«»„“')
    name = re.sub(r'\s+', ' ', name)
    
    # 5. Приводим к верхнему регистру
    name = name.upper()
    
    return name.strip()


