def normalize_company_names(df: pd.DataFrame) -> pd.DataFrame:
    # 1. Нормализуем короткое название
    if 'short_name' in df.columns:
        df['short_name'] = df['short_name'].apply(clean_short_name)
    elif 'full_name' in df.columns:
        # Если короткого нет, делаем из полного
        df['short_name'] = df['full_name'].apply(clean_short_name)
    else:
        df['short_name'] = ''
    
    # 2. Нормализуем полное название
    if 'full_name' in df.columns:
        df['full_name'] = df['full_name'].apply(clean_full_name)
    else:
        df['full_name'] = ''
    
    return df

def clean_address(address):
    """

    Принимает:
        address (str) - исходный адрес из CSV.

    Выполняет:
        - удаляет лишние пробелы и переносы строк;
        - приводит адрес к нижнему регистру;
        - нормализует обозначения улиц;
        - приводит дом, корпус и строение к единому виду;
        - сохраняет номер дома, корпуса и строения.

    Возвращает:
        str - очищенный адрес формата:
        "название улицы, номер дома стрномер корпномер"
    """

    if pd.isna(address):
        return address

    # убираем переносы и лишние пробелы
    address = re.sub(r'[\n\r\t]+', ' ', address)
    address = re.sub(r'\s+', ' ', address).strip()

    # нормализуем тип улицы
    address = re.sub(r'\b(улица|ул\.)\s*', '', address)

    # приводим строение к стр1
    address = re.sub(
        r'\b(строение|стр\.?|ст\.?|с)\s*(\d+\w*)',
        r'стр\2',
        address
    )

    # приводим корпус к корп1
    address = re.sub(
        r'\b(корпус|корп\.?|к\.)\s*(\d+\w*)',
        r'корп\2',
        address
    )

    # убираем обозначение дома
    address = re.sub(
        r'\b(дом|д\.)\s*',
        '',
        address
    )

    # нормализуем запятые
    address = re.sub(r'\s*,\s*', ', ', address)

    # убираем лишние пробелы
    address = re.sub(r'\s+', ' ', address)

    return address.strip(" ,")